class AiMessageRepository:
    """Data-access layer for the ai_messages table."""

    def __init__(self, session: AsyncSession) -> None:
        self._session = session

    async def create(
        self,
        conversation_id: uuid.UUID,
        role: AiMessageRole,
        content: str,
        token_count: int | None = None,
        tool_calls: list[ToolCallTrace | dict[str, object] | str] | None = None,
    ) -> AiMessage:
        """
        Insert a new message row.

        Args:
            conversation_id: Parent conversation UUID.
            role: Message role (user/assistant/system/tool).
            content: Raw text content of the message.
            token_count: Optional LLM token usage for this message.
            tool_calls: Ordered structured tool traces used to produce the message.

        Returns:
            The persisted AiMessage instance.
        """
        message = AiMessage(
            conversation_id=conversation_id,
            role=role,
            content=content,
            token_count=token_count,
            tool_calls=[
                item.model_dump(mode="json") if isinstance(item, ToolCallTrace) else item
                for item in list(tool_calls or [])
            ],
        )
        self._session.add(message)
        await self._session.flush()
        await self._session.refresh(message)
        return message

    async def list_for_conversation(
        self,
        conversation_id: uuid.UUID,
    ) -> Sequence[AiMessage]:
        """
        Return all messages in a conversation ordered by creation time (ascending).

        Args:
            conversation_id: The target conversation UUID.

        Returns:
            Ordered list of AiMessage instances.
        """
        result = await self._session.execute(
            select(AiMessage)
            .where(AiMessage.conversation_id == conversation_id)
            .order_by(AiMessage.created_at.asc())
        )
        return result.scalars().all()

    async def update_token_count(
        self,
        message_id: uuid.UUID,
        token_count: int,
    ) -> None:
        """
        Patch the token_count field of an existing message after the LLM finishes.

        Args:
            message_id: Target message UUID.
            token_count: Final token count from the LLM response.
        """
        await self._session.execute(
            update(AiMessage).where(AiMessage.id == message_id).values(token_count=token_count)
        )


