class AiConversationRepository:
    """
    Data-access layer for the ai_conversations table.

    All methods accept a SQLAlchemy AsyncSession so the caller can
    control transaction boundaries (auto-committed by the FastAPI
    dependency via get_db_session).
    """

    def __init__(self, session: AsyncSession) -> None:
        self._session = session

    async def create(
        self,
        document_id: uuid.UUID,
        user_id: uuid.UUID,
        title: str | None = None,
    ) -> AiConversation:
        """
        Insert a new conversation row and return it with all defaults populated.

        Args:
            document_id: The document this conversation is attached to.
            user_id: The authenticated user who owns this conversation.
            title: Optional display title (auto-generated by the caller if None).

        Returns:
            The persisted AiConversation instance.
        """
        conversation = AiConversation(
            document_id=document_id,
            user_id=user_id,
            title=title,
        )
        self._session.add(conversation)
        await self._session.flush()  # populate server-default fields (id, created_at)
        await self._session.refresh(conversation)
        return conversation

    async def get_by_id(self, conversation_id: uuid.UUID) -> AiConversation | None:
        """
        Fetch a single conversation by its primary key.

        Returns None if not found.
        """
        result = await self._session.execute(
            select(AiConversation).where(AiConversation.id == conversation_id)
        )
        return result.scalar_one_or_none()

    async def get_by_id_for_user(
        self,
        conversation_id: uuid.UUID,
        user_id: uuid.UUID,
    ) -> AiConversation | None:
        """
        Fetch a single conversation by id, scoped to an owner user.

        Args:
            conversation_id: Conversation UUID.
            user_id: Owner user UUID.

        Returns:
            AiConversation when found and owned by the user, otherwise None.
        """
        result = await self._session.execute(
            select(AiConversation).where(
                (AiConversation.id == conversation_id) & (AiConversation.user_id == user_id)
            )
        )
        return result.scalar_one_or_none()

    async def list_for_document(
        self,
        document_id: uuid.UUID,
        user_id: uuid.UUID,
        limit: int = 50,
        offset: int = 0,
    ) -> tuple[Sequence[AiConversation], int]:
        """
        List conversations for a specific document and user, newest first.

        Args:
            document_id: Scope to this document.
            user_id: Scope to this user.
            limit: Maximum rows to return.
            offset: Number of rows to skip for pagination.

        Returns:
            A (rows, total_count) tuple.
        """
        base_where = (AiConversation.document_id == document_id) & (
            AiConversation.user_id == user_id
        )

        count_result = await self._session.execute(
            select(func.count()).select_from(AiConversation).where(base_where)
        )
        total: int = count_result.scalar_one()

        rows_result = await self._session.execute(
            select(AiConversation)
            .where(base_where)
            .order_by(AiConversation.updated_at.desc())
            .limit(limit)
            .offset(offset)
        )
        rows = rows_result.scalars().all()

        return rows, total

    async def delete(self, conversation_id: uuid.UUID) -> bool:
        """
        Hard-delete a conversation (cascades to messages and suggestions).

        Returns True if a row was deleted, False if not found.
        """
        conversation = await self.get_by_id(conversation_id)
        if conversation is None:
            return False
        await self._session.delete(conversation)
        return True

    async def touch_updated_at(self, conversation_id: uuid.UUID) -> None:
        """
        Update the conversation updated_at timestamp to now().

        Called whenever a new message is appended so ordering by recency works.
        """
        await self._session.execute(
            update(AiConversation)
            .where(AiConversation.id == conversation_id)
            .values(updated_at=func.now())
        )

    async def set_title(self, conversation_id: uuid.UUID, title: str) -> None:
        """
        Persist a generated title only when the conversation is still untitled.

        This avoids overwriting a title that may have been set concurrently.
        """
        await self._session.execute(
            update(AiConversation)
            .where((AiConversation.id == conversation_id) & (AiConversation.title.is_(None)))
            .values(title=title)
        )


